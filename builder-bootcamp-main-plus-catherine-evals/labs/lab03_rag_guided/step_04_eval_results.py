class RAGEvalItem(BaseModel):
    """
    Structured schema for a single evaluation item used by the Evals API.

    This mirrors the JSONL produced in Step 3 and is passed as the
    `item_schema` for a custom data source. If you extend the dataset
    format (e.g., additional metadata fields), update this model
    accordingly.
    """

    input: str = Field(..., description="The input question or prompt for the item.")
    expected_answer: str = Field(..., description="The expected/ground-truth answer text for the item.")
    model_answer: str = Field(..., description="The model's generated answer to evaluate.")
    expected_tool: Optional[str] = Field(None, description="The expected tool for the answer, if available.")
    expected_category: Optional[str] = Field(None, description="The expected category for the answer, if available.")
    item_key: Optional[str] = Field(None, description="Unique identifier for the item, if available.")
    source: Optional[str] = Field(None, description="Source file or context, if available.")

def load_rag_results(filepath: str) -> List[Dict[str, Any]]:
    """
    Load evaluation items from a JSONL file produced by Step 3.

    Each non-empty line is expected to be a JSON object shaped as:
        {"item": {"input": "...", "expected_answer": "...", "model_answer": "...", ...}}

    Args:
        filepath: Absolute path to the JSONL file.

    Returns:
        List[Dict[str, Any]]: A list of item dictionaries with available fields.

    Notes:
        - Lines that are blank are skipped.
        - Missing optional fields are populated with None to align with the schema.
        - The caller is responsible for existence checks and handling an empty result.
    """
    items: List[Dict[str, Any]] = []
    with open(filepath, "r", encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            obj = json.loads(line)
            item = obj.get("item", {})
            # Collect all possible fields, defaulting to empty string or None as appropriate
            items.append(
                {
                    "input": str(item.get("input", "")),
                    "expected_answer": str(item.get("expected_answer", "")),
                    "model_answer": str(item.get("model_answer", "")),
                    "expected_tool": str(item.get("expected_tool", "")) if "expected_tool" in item else None,
                    "expected_category": str(item.get("expected_category", "")) if "expected_category" in item else None,
                    "item_key": str(item.get("item_key", "")) if "item_key" in item else None,
                    "source": str(item.get("source", "")) if "source" in item else None,
                }
            )
    return items

def _wait_for_run_completion(
    client: OpenAI,
    eval_id: str,
    run_id: str,
    poll_interval_seconds: int = 2,
    timeout_seconds: int = 600,
) -> Any:
    """
    Poll an eval run until it reaches a terminal state or times out.

    Terminal statuses include: "completed", "failed", and "canceled".

    Args:
        client: Pre-initialized OpenAI client.
        eval_id: Identifier of the eval definition.
        run_id: Identifier of the eval run to poll.
        poll_interval_seconds: Delay between status checks.
        timeout_seconds: Maximum time to wait before raising a timeout.

    Returns:
        Any: The final eval run object as returned by the client SDK.

    Raises:
        TimeoutError: If the run does not reach a terminal state within the timeout.
    """
    start_time = time.time()
    terminal_statuses = {"completed", "failed", "canceled"}
    while True:
        eval_run = client.evals.runs.retrieve(run_id, eval_id=eval_id)
        status = getattr(eval_run, "status", None)
        print(f"Eval run status: {status}")
        if status in terminal_statuses:
            return eval_run
        if time.time() - start_time > timeout_seconds:
            raise TimeoutError(f"Timed out after {timeout_seconds}s waiting for eval run {run_id}")
        time.sleep(poll_interval_seconds)

def main() -> None:
    """
    Run the RAG evaluation using the dataset produced by Step 3.

    This function resolves the dataset path, loads items, creates an eval
    with the configured rubric, runs it, waits for completion, and prints
    both the final status and a compact score summary.
    """
    # Resolve dataset path: allow env override for flexibility across runs
    default_data_path = os.path.abspath(
        os.path.join(os.path.dirname(__file__), "../data/rag_model_answer.jsonl")
    )
    rag_file = os.getenv("RAG_EVAL_DATA_FILE", default_data_path)
    rag_file = os.path.abspath(rag_file)

    if not os.path.isfile(rag_file):
        print(f"RAG results file not found: {rag_file}")
        sys.exit(1)

    # Load items to evaluate
    items = load_rag_results(rag_file)
    if not items:
        print("No items loaded from rag_model_answer.jsonl. Exiting.")
        sys.exit(1)

    # Initialize client
    try:
        client = OpenAI()
    except Exception as client_error:
        print(f"Failed to initialize OpenAI client: {client_error}")
        sys.exit(1)

    # Configure names and runtime knobs from environment
    run_uuid = str(uuid.uuid4())
    eval_name_prefix = os.getenv("EVAL_NAME_PREFIX", "RAG-Model-Answer-Alignment")
    eval_run_name = os.getenv("EVAL_RUN_NAME", "RAG Alignment eval run")
    poll_interval_seconds = int(os.getenv("EVAL_POLL_INTERVAL_SECONDS", "2"))
    timeout_seconds = int(os.getenv("EVAL_TIMEOUT_SECONDS", "600"))

    print(f"Run UUID: {run_uuid}")
    print(f"Loaded {len(items)} items from: {rag_file}")
    print(f"Polling every {poll_interval_seconds}s (timeout {timeout_seconds}s)")

    # Create an eval definition using a custom schema and inline testing criteria
    try:
        eval_obj = client.evals.create(
            name=f"{eval_name_prefix}-{run_uuid}",
            data_source_config={
                "type": "custom",
                "item_schema": RAGEvalItem.model_json_schema(),
            },
            testing_criteria=rag_testing_criteria,
        )
        eval_id = getattr(eval_obj, "id", "")
        print("Created eval definition.")
    except Exception as create_error:
        print(f"Failed to create eval definition: {create_error}")
        sys.exit(1)

    # Provide items inline as JSONL content, including all possible fields
    file_content = [{"item": entry} for entry in items]
    try:
        eval_run = client.evals.runs.create(
            eval_id=eval_obj.id,
            name=f"{eval_run_name} ({run_uuid})",
            data_source={
                "type": "jsonl",
                "source": {"type": "file_content", "content": file_content},
            },
        )
        print(
            f"Started eval run: {getattr(eval_run, 'id', 'unknown')} "
            f"(status={getattr(eval_run, 'status', 'unknown')})"
        )
    except Exception as run_error:
        print(f"Failed to start eval run: {run_error}")
        sys.exit(1)

    # Wait for terminal status
    try:
        eval_run = _wait_for_run_completion(
            client,
            eval_obj.id,
            eval_run.id,
            poll_interval_seconds=poll_interval_seconds,
            timeout_seconds=timeout_seconds,
        )
    except TimeoutError as timeout_error:
        print(str(timeout_error))
        print("Tip: Increase EVAL_TIMEOUT_SECONDS and try again.")
        sys.exit(1)
    except Exception as wait_error:
        print(f"Error while polling eval run: {wait_error}")
        sys.exit(1)

    print("Eval run finished with status:", getattr(eval_run, "status", "unknown"))

    if getattr(eval_run, "status", None) == "completed":
        try:
            passed, total = evaluation_run_results.get_eval_run_score(eval_run.id, eval_obj.id)
            if passed is not None and total is not None:
                print(f"Eval run score: {passed} / {total} passed")
                if eval_id:
                    print(f"Navigate to https://platform.openai.com/evaluation/evals/{eval_id} to see the evaluation run")
        except Exception as score_error:
            print(f"Failed to retrieve eval score: {score_error}")
    else:
        error_detail = getattr(eval_run, "error", None)
        if error_detail:
            print(f"Eval run error detail: {error_detail}")

    # Provide a pointer to the dashboard for interactive review
    print("View details in the Evaluations dashboard: https://platform.openai.com/evaluations")

if __name__ == "__main__":
    main()
